# (18°) Use operadores lógicos (and, or, not) para avaliar três expressões verdadeiras e falsas.

# Usando operador AND
if (1 + 1 and 2 + 0) == 2:
    print("Verdadeiro")
else:
    print("Falso")

# Usando operador OR
if (1 + 1 or 2 + 1) == 2:
    print("Verdadeiro")
else:
    print("Falso")

# Usando operador NOT
if not 1 + 1 == 2:
    print("Verdadeiro")
else: 
    print("Falso")


